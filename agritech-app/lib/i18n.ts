export function getTranslation(lang: Language, key: string): string {
    const keys = key.split('.');
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    let value: any = (translations as any)[lang];

    for (const k of keys) {
        value = value?.[k];
    }

    return value || key;
}
